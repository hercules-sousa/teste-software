public class Raizes {
    private double x1;
    private double x2;

    public Raizes(double x1, double x2) {
        this.x1 = x1;
        this.x2 = x2;
    }

    @Override
    public String toString() {
        return "Raizes{" +
                "x1=" + x1 +
                ", x2=" + x2 +
                '}';
    }
}
